<template>
  <div class="input-group mb-3">
    <textarea
      type="text"
      class="form-control"
      placeholder="Please write modify reply"
      aria-label="Recipient's username"
      aria-describedby="button-addon2"
      style="height:2.5rem"
      v-model="replyUpdateData.content"
    />
    <div class="input-group-append">
      <button class="btn btn-outline-success" type="button" id="button-addon2" @click="replyModify">수정</button>
    </div>
  </div>
</template>

<script>
import axios from 'axios'
const baseURL = process.env.VUE_APP_BACKURL;
export default {
  data() {
    return {
      replyUpdateData:[],
    }
  },
    props: {
        reply: Object,
    },
    methods: {
        replyModify() {
            Swal.fire({
              width: 350,
              text: "답글을 수정하시겠습니까?",
              icon: 'warning',
              showCancelButton: true,
              confirmButtonColor: '#3085d6',
              cancelButtonColor: '#d33',
              confirmButtonText: '<a style="font-size:1rem; color:black">Udate</a>',
              cancelButtonText: '<a style="font-size:1rem; color:black">Cancel</a>'
              }).then((result) => {
                if (result.value) {
                  const Toast = Swal.mixin({
                    toast: true,
                    position: 'top-end',
                    showConfirmButton: false,
                    timer: 1000,
                    timerProgressBar: true,
                    onOpen: (toast) => {
                      toast.addEventListener('mouseenter', Swal.stopTimer)
                      toast.addEventListener('mouseleave', Swal.resumeTimer)
                    }
                  })
                axios.put(`${baseURL}/reply/reModify`,this.replyUpdateData)
                  .then(() => {
                    Toast.fire({
                      icon: 'success',
                      title: '답글 수정 완료!'
                    })
                    setTimeout(() => {
                      this.$router.go()
                    },1000)
                  }).catch((error) => {
                    console.log(error.response.data)
                  })
                }
              })
        },
    },
    created() {
      this.replyUpdateData = this.reply
    },
}
</script>

